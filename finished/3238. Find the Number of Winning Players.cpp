class Solution {
public:
    int winningPlayerCount(int n, vector<vector<int>> &pick) {
        array<array<int, 12>, 10> pick_balls;
        for (const auto &p : pick) pick_balls[p[0]][p[1]]++;
        auto ans = 0;
        for (int i = 0; i < 10; i++)
            for (const auto &b : pick_balls[i])
                if (b > i) {
                    ans++;
                    break;
                }
        return ans;
    }
};