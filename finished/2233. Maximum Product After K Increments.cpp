class Solution {
    constexpr static int mod = 1e9 + 7;
public:
    int maximumProduct(vector<int> &nums, int k) {
        auto ans = 1ll;
        priority_queue<int, vector<int>, greater<int>> pq(nums.begin(), nums.end());
        while (k--) {
            auto x = pq.top();
            pq.pop();
            pq.push(x + 1);
        }
        while (!pq.empty()) {
            if (!pq.top()) return 0;
            ans = (ans * pq.top()) % mod;
            pq.pop();
        }
        return ans;
    }
};