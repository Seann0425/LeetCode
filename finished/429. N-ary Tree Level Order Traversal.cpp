class Solution {
public:
    vector<vector<int>> levelOrder(Node *root) {
        if (!root) return {};
        vector<vector<int>> ans;
        queue<Node *> q;
        q.push(root);
        while (!q.empty()) {
            auto size = q.size();
            ans.emplace_back();
            while (size--) {
                auto node = q.front();
                q.pop();
                ans.back().push_back(node->val);
                for (const auto &child : node->children) q.push(child);
            }
        }
        return ans;
    }
};