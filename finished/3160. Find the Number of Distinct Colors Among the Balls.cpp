class Solution {
public:
    vector<int> queryResults(size_t limit, vector<vector<int>> &queries) {
        const auto m = queries.size();
        unordered_map<int, int> ball;
        unordered_map<int, int> colors;
        vector<int> ans;
        ans.reserve(m);
        for (const auto &q : queries) {
            const auto b = q[0];
            const auto c = q[1];
            if (ball[b]) {
                if (colors[ball[b]] == 1) colors.erase(ball[b]);
                else --colors[ball[b]];
            }
            colors[c]++;
            ball[b] = c;
            ans.push_back(colors.size());
        }
        return ans;
    }
};