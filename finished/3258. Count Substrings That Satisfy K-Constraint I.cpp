class Solution {
public:
    int countKConstraintSubstrings(string s, int k) {
        const auto n = s.size();
        auto zero_count = 0, one_count = 0;
        auto ans = 0uz;
        for (auto l = 0uz, r = 0uz; r < n; r++) {
            (s[r] == '0' ? zero_count : one_count)++;
            while (min(zero_count, one_count) > k) (s[l++] == '0' ? zero_count : one_count)--;
            ans += r - l + 1;
        }
        return static_cast<int>(ans);
    }
};