class Solution {
public:
    long long maxKelements(vector<int> &nums, int k) {
        priority_queue<int, vector<int>> pq(nums.begin(), nums.end());
        long long ans = 0;
        for (int i = 0; i < k; i++) {
            auto top = pq.top();
            pq.pop();
            ans += top;
            pq.push((top + 2) / 3);
        }
        return ans;
    }
};