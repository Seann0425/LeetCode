class Solution {
public:
    long long maximumImportance(int n, vector<vector<int>>& roads) {
        vector<int> degree(n);
        for (const auto& road : roads) {
            degree[static_cast<size_t>(road.front())]++;
            degree[static_cast<size_t>(road.back())]++;
        }
        sort(degree.begin(), degree.end());
        long long ans = 0;
        for (int i = n; i > 0; i--) {
            ans += static_cast<long long>(i) * static_cast<long long>(degree[static_cast<size_t>(i - 1)]);
        }
        return ans;
    }
};