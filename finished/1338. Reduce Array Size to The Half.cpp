class Solution {
public:
    int minSetSize(vector<int> &arr) {
        const auto n = arr.size(); // guaranteed even
        unordered_map<int, int> cnt;
        for (const auto &i : arr) cnt[i]++;
        vector<pair<int, int>> v;
        for (const auto &[val, freq] : cnt) v.emplace_back(freq, val);
        sort(v.begin(), v.end(), greater<>());
        auto ans = 0uz, sum = 0uz;
        while (sum < n / 2) sum += v[ans++].first;
        return ans;
    }
};