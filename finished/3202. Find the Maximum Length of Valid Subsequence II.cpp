#include <ranges>
class Solution {
public:
    int maximumLength(vector<int> &nums, int k) {
        vector<vector<int>> dp(k, vector<int>(k)); // sum, tail
        for (auto num : nums) {
            num %= k;
            for (auto sum = 0; sum < k; ++sum) {
                auto tail = (sum - num + k) % k;
                dp[sum][num] = max(dp[sum][num], dp[sum][tail] + 1);
            }
        }
        return ranges::max(ranges::join_view(dp));
    }
};