class Solution {
public:
    long long minSum(vector<int> &nums1, vector<int> &nums2) {
        auto sum = 0ll, ans = 0ll;
        auto has_zero1 = false, has_zero2 = false;
        for (const auto &x : nums1) sum += x ? x : (has_zero1 = true);
        ans = sum;
        for (const auto &x : nums2) sum -= x ? x : (has_zero2 = true);
        if (sum > 0 and !has_zero2 or sum < 0 and !has_zero1) return -1ll;
        return ans - min(sum, 0ll);
    }
};