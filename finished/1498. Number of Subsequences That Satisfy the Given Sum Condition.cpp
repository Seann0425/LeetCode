class Solution {
public:
    int numSubseq(vector<int> &nums, int target) {
        sort(nums.begin(), nums.end());

        const auto n = nums.size();
        const auto mod = static_cast<int>(1e9 + 7);
        vector<int> power(n, 1);
        for (auto i = 1uz; i < n; ++i) power[i] = (power[i - 1] * 2) % mod;

        auto ans = 0;
        for (auto i = 0uz, j = n - 1; i < n; ++i) {
            while (j < n && j >= i && nums[i] + nums[j] > target) --j;
            if (j < i or j > n) break;
            ans = (ans + power[j - i]) % mod;
        }
        return ans;
    }
};
