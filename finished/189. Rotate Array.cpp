class Solution {
    void reverse(vector<int> &nums, size_t start, size_t end) {
        while (start < end) swap(nums[start++], nums[end--]);
    }
public:
    void rotate(vector<int> &nums, size_t k) {
        const auto n = nums.size();
        if (n == 1) return;
        k %= n;
        if (!k) return;
        reverse(nums, 0, n - 1);
        if (k > 1) reverse(nums, 0, k - 1);
        reverse(nums, k, n - 1);
    }
};