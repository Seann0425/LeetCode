// kadane's algorithm
class Solution {
public:
    int maxAbsoluteSum(vector<int> &nums) {
        auto max_sum = nums[0], min_sum = nums[0];
        auto sum_mx = nums[0], sum_mn = nums[0];
        for (const auto &i : nums | views::drop(1)) {
            sum_mx = max(i, sum_mx + i);
            max_sum = max(max_sum, sum_mx);
            sum_mn = min(i, sum_mn + i);
            min_sum = min(min_sum, sum_mn);
        }
        return max(abs(max_sum), abs(min_sum));
    }
};