class Solution {
public:
    int minEatingSpeed(vector<int> &piles, int h) {
        auto l = 1, r = *max_element(piles.begin(), piles.end());
        while (l < r) {
            auto m = l + (r - l) / 2;
            auto t = 0ll;
            for (const auto &p : piles) t += (p + m - 1) / m; // ceiling
            if (t <= h) r = m;
            else l = m + 1;
        }
        return r;
    }
};