#include <ranges>
class Solution {
public:
    int maxNumOfMarkedIndices(vector<int> &nums) {
        if (nums.size() == 1) return 0;
        const auto n = nums.size();
        sort(nums.begin(), nums.end());
        auto cmp = [&](int dummy, int k) -> bool {
            for (auto i = 0; i < k; ++i) {
                if (i >= n or n - k + i >= n) return true;
                if (2 * nums[i] > nums[n - k + i]) return true;
            }
            return false;
        };
        return (*ranges::upper_bound(views::iota(0, static_cast<int>(n) / 2 + 1), 0, cmp) - 1) * 2;
    }
};