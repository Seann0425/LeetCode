class Solution {
public:
    int countPrimes(size_t n) {
        vector<bool> is_prime(n);
        is_prime[0] = is_prime[1] = false;
        for (size_t i = 2; i < n; i++) {
            is_prime[i] = true;
            for (size_t j = i * 2; j < n; j += i) is_prime[j] = false;
        }
        return count(is_prime.begin(), is_prime.end(), true);
    }
};